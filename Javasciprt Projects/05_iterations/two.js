// While Loop with index
let index = 0;
while (index <= 10) {
    // console.log(index);
    index = index + 2;
}

//while loog with array
let array1 = ["Superman", "Batman", "Spiderman", "Ironman"];
let item = 0;
while (item < array1.length) {
    // console.log(array1[item]);
    item = item + 1;    
}

